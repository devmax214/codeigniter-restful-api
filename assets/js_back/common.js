function checkExtention(filename, types) {
	var ext = filename.substring(filename.length-4, filename.length);
	if($.inArray(ext, types) == -1) {
		return false;
	}
	else {
		return true;
	}
}

function toggle_area(el, sp) {
	if($(el).is(":hidden")) 
		$(el).show(sp);
	else
		$(el).hide(sp);
}

function numberWithCommas(x) {
    var parts = x.toString().split(".");
    parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ",");
    return parts.join(".");
}

function checkPointerInElement(event, el) {
	if(event.pageX >= $(el).offset().left 
	  && event.pageX <= $(el).offset().left + parseInt($(el).css("width"),10)
	  && event.pageY >= $(el).offset().top 
	  && event.pageY <= $(el).offset().top + parseInt($(el).css("height"),10)) {
		return true;
	}
	else
		return false;
}

function getUnsignalValue(value, max) {
	value = Math.round(value);
	
	if(value>max)
		value = max;
	if(value<0)
		value = 0;
	if(value<10)
		value = "0" + value;
	return value;
}

